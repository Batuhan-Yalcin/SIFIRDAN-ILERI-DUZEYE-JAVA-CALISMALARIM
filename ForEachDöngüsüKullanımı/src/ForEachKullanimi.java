
public class ForEachKullanimi {

	public static void main(String[] args) {
		
		
		// Foreach döngüsü diziler ile birlikte kullanılan döngü çeşididir.
		// Eğer ki notlarımı okuyor iseniz bunu direkt geçin önce Diziler notlarıma bakın daha sonra buna bakın.
		// Ben kendime notlar bıraktığım için Foreach döngüsünü de döngülerin altına eklemek istedim..
		
		//  FOREACH DÖNGÜSÜ BİR DİZİDEKİ KİŞİLERİN İSİMLERİNİ YADA DİZİ KAÇ ELEMANLI KAÇINCI İNDEXTE VS. BUNLARI GÖSTERİR.
		
		String isimler [] = {"Batuhan", "Ahmet","Gül","Şevval","Osman"};
		
		for (String isim : isimler)
		{
		System.out.println(isim);	
		}
		

	}

}
