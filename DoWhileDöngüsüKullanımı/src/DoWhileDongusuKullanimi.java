
public class DoWhileDongusuKullanimi {

	public static void main(String[] args) {
		
		
		// Başlangıç yine dışarıya
		int i = 1;
		// ardından do yazıp süslü parantez açıyoruz içine ekrana yazdırma ve arttırmayı yapıyoruz
		do {
			System.out.println(i);
			i++;
		} /* do parantezinden çıkıp while ın içine koşulu yazıp ; ekliyoruz */ while (i <= 10);
		

	}

}
