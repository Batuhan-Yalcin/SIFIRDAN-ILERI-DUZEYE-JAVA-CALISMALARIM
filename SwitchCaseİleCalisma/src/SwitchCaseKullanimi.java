
public class SwitchCaseKullanimi {

	public static void main(String[] args) {
		
		
		// Switch Blokları İf Bloklarına Göre daha az kullanılan yöntemlerdir.
		// Bir koşul yazarız ve case ler ile koşulları kontrol ederiz gelin birlikte bakalım..
		
		
		String not = "AA";
		
		
		switch (not)
		{
		case "BA" :
			System.out.println("Not BA dır.");
			break;
		case "CB":
			System.out.println("Not CB dir.");
			break;
		case "AA":
			System.out.println("Not AA dır.");
		}
		

	}

}
