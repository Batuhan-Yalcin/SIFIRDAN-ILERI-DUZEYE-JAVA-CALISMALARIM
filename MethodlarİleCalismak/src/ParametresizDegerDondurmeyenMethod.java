
public class ParametresizDegerDondurmeyenMethod {

	public static void main(String[] args) {
		
		
		
		// Void Geriye Değer döndürmez String olsun int olsun bir değeri yoktur sadece çıktı verir.
		
		selamVer();
		adiniYaz();
		sayiGirme();

	}

	static void selamVer()
	{
		System.out.println("Merhabalar..");
	}
	
	static void adiniYaz()
	{
		System.out.println("Adım : Batuhan Yalçın");
	}
	
	static void sayiGirme() {
		System.out.println(58);
	}
}
