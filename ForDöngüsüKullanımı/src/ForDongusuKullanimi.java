
public class ForDongusuKullanimi {

	public static void main(String[] args) {
	
		// 0 dan başlayıp 10 a kadar olan sayıları yazma
		
		for (int i = 0; i < 10; i++)
		{
			System.out.println("Burada 0 dan başlayıp 10 a kadar ekrana çıktı veren döngü yazdık Sayi :  " +i);
		}
	
		// 1 den 10 a kadar batuhan yazma 
		for(int i =1; i <10; i++)
		{
			System.out.println("Batuhan"+i);
		}
		
		
		
	}

}
